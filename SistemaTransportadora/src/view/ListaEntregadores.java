    private void btMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btMostrarActionPerformed
        List<Entregador> lent = new ArrayList<Entregador>();
        DefaultTableModel modelo = (DefaultTableModel) tbTabelaEntregadores.getModel();
        modelo.setRowCount(0);
        
        ConectaBD banco = new ConectaBD();
        boolean con = banco.getConnection();
        if(con == true){
            try {
                PreparedStatement pst = banco.connection.prepareStatement("SELECT * FROM entregador ORDER BY codigo");
                ResultSet rst = pst.executeQuery();
                while(rst.next()){
                    Entregador ent = new Entregador();
                    
                    ent.setCodigo(rst.getInt("codigo"));
                    ent.setNome(rst.getString("nome"));
                    
                    lent.add(ent);
                }
                for(int i = 0; i<=lent.size(); i++){
                    modelo.insertRow(i, new Object[]{lent.get(i).getCodigo(), lent.get(i).getNome()});
                }
                pst.close();
                rst.close();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Erro de Consulta\n"+ex.toString());
            }
            banco.close();
        }
        else{
            JOptionPane.showMessageDialog(null, "Erro de conex찾o com o banco de dados");
        }
        
    }//GEN-LAST:event_btMostrarActionPerformed

